package org.example.GUI.ui;

import org.example.GUI.mainGame.Game;

/**
 * Contains constants related to the user interface buttons.
 */
public class Buttons {

    /**
     * Nested class representing the UI elements.
     */
    public static class UI {

        /**
         * Nested class representing a button and its dimensions.
         */
        public static class Button {
            /**
             * The default width of the button.
             */
            public static final int B_WIDTH_DEFAULT = 140;

            /**
             * The default height of the button.
             */
            public static final int B_HEIGHT_DEFAULT = 56;

            /**
             * The current width of the button.
             */
            public static final int B_WIDTH = B_WIDTH_DEFAULT;

            /**
             * The current height of the button.
             */
            public static final int B_HEIGHT = B_HEIGHT_DEFAULT;
        }
    }
}
